
# Bitwise Operators


a = 5  # 0101
b = 3  # 0011
print("\nBitwise Operators:")
print("a & b:", a & b)  # 0001
print("a | b:", a | b)  # 0111
print("a ^ b:", a ^ b)  # 0110
print("~a:", ~a)        # -6 (inverted)
print("a << 1:", a << 1)  # 1010 -> 10
print("a >> 1:", a >> 1)  # 0010 -> 2



